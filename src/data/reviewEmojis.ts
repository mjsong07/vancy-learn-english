const irregularForms: Record<string, string> = {
  children: "child",
  feet: "foot",
  geese: "goose",
  mice: "mouse",
  people: "person",
  teeth: "tooth",
  women: "woman",
  men: "man"
};

export function findEnglishEmoji(english: string) {
  const normalized = normalizeEnglish(english);
  if (!normalized) return "";

  const exactEmoji = englishEmojiMap[normalized];
  if (exactEmoji) return exactEmoji;

  if (!normalized.includes(" ")) {
    const baseWord = irregularForms[normalized] || getBaseWord(normalized);
    return englishEmojiMap[baseWord] || "";
  }

  return "";
}

function normalizeEnglish(value: string) {
  return value
    .toLocaleLowerCase()
    .replace(/[’']/g, "")
    .replace(/[^a-z0-9\s-]/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/^(?:a|an|the)\s+/, "");
}

function getBaseWord(word: string) {
  const ingStem = word.replace(/ing$/, "");
  const edStem = word.replace(/ed$/, "");
  const candidates = [
    word.replace(/ies$/, "y"),
    word.replace(/ied$/, "y"),
    word.replace(/ves$/, "f"),
    ingStem,
    `${ingStem}e`,
    removeRepeatedFinalLetter(ingStem),
    edStem,
    `${edStem}e`,
    removeRepeatedFinalLetter(edStem),
    word.replace(/es$/, ""),
    word.replace(/s$/, "")
  ];
  return candidates.find((candidate) => englishEmojiMap[candidate]) || word;
}

function removeRepeatedFinalLetter(value: string) {
  return value.replace(/([a-z])\1$/, "$1");
}
